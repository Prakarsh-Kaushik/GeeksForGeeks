class Solution
{
    public:
    //Function to reverse words in a given string.
    string reverseWords(string S) 
    { 
        // code here 
        string word;
        string res;
        stringstream ss(S);
        
        while(getline(ss,word, '.')){
            res = word + "." + res;
        }
        res.pop_back();
        return res;
    } 
};

